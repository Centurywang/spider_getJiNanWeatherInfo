function calc(idx) {
    var ls = locinfo.substr(idx*4, 4), valb = new Array();
    for (i = 0; i < 4; i++) {
        valb[i] = ls.charCodeAt(i);
        if (valb[i] == 33)
            valb[i] = 60;
    }
    return {
        x: (valb[2] + 35) * 100 + valb[3] - 35,
        y: (valb[0] - 35) * 100 + valb[1] - 35,
        toString: function () { return "(" + this.x + ", " + this.y + ")" }
    };
}

function getmin(num) {
    var sign = Math.abs(num) / num;
    var degree = Math.floor(Math.abs(num) / 100) * sign;
    var minute = Math.floor((Math.abs(num) % 100));
    return {
        degree: degree,
        minute: minute,
        value: minute / 60 + degree,
        toString: function () {
            return this.degree + "°" + this.minute + "′";
        }
    };
}

function padding(str, width, fillchar, cut) {
    fillchar = fillchar || " ";
    cut = cut || false;
    width = width || 0;
    var posit = (width < 0);
    width = Math.abs(width);
    while(str.length < width){
        if(posit) str += fillchar;
        else str = fillchar + str;
    }
    if(cut){
        if(posit) str = str.substr(0, width);
        else str = str.substr(str.length - width);
    }
    return str;
}

function getpos(addr1, addr2) {
    if (addrinfo[addr1]) {
        if (addrinfo[addr1][addr2]) {
            var pos = calc(addrinfo[addr1][addr2]);
            pos.toString = function(){
                return (getmin(this.x) +"("+this.x+"),"+ getmin(this.y)+"("+this.y+")");
            }
            return pos;
        }
    }
}